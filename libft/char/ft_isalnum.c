int	ft_isalnum(int c)
{
	return ((c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9'));
}

int	ft_isnum(int c)
{
	return ((c >= '0' && c <= '9'));
}
